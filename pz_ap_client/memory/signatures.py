# ── robust full-module AOB scan (pymem's pattern_scan_module misses high RVAs) ───────────────────────
def _compile_aob(sig: str) -> re.Pattern:
    parts = []
    for tok in sig.split():
        parts.append(b"." if tok == "??" else re.escape(bytes([int(tok, 16)])))
    return re.compile(b"".join(parts), re.DOTALL)


def module_aob_scan(scanner, sig: str, max_hits: int = 4) -> List[int]:
    """Scan the whole main module for an AOB pattern; returns up to max_hits match addresses.
    Reads in overlapping chunks so a match never straddles a boundary."""
    base = getattr(scanner, "module_base", None)
    size = getattr(scanner, "module_size", None) or 0x20000000
    if not base:
        return []
    pat = _compile_aob(sig)
    plen = len(sig.split())
    hits: List[int] = []
    chunk = 0x800000
    addr = base
    end = base + size
    while addr < end and len(hits) < max_hits:
        n = min(chunk + plen, end - addr)
        try:
            data = scanner.read_bytes(addr, n)
        except Exception:
            addr += chunk
            continue
        for m in pat.finditer(data):
            hits.append(addr + m.start())
            if len(hits) >= max_hits:
                break
        addr += chunk
    return hits


# ── self-check ───────────────────────────────────────────────────────────────────────────────────────
def _is_leaked_detour(scanner, site: int, original: bytes) -> bool:
    """True iff ``site`` holds one of OUR OWN leaked detours: a rel32 jmp (0xE9) whose target
    trampoline still contains the site's original bytes. This distinguishes a hook leaked by a prior
    unclean exit (the console X button, a kill, a crash) - which we may safely restore in place - from
    a genuine code change or a foreign patch, which we must NOT touch."""
    try:
        head = scanner.read_bytes(site, 5)
        if head[0] != 0xE9:
            return False
        rel = int.from_bytes(head[1:5], "little", signed=True)
        target = site + 5 + rel
        tramp = scanner.read_bytes(target, 0x200)
    except Exception:
        return False
    return original in tramp  # our trampoline embeds the relocated original instruction


def recover_leaked_hook(scanner, name: str) -> bool:
    """If a hook site still holds our own leaked detour from a prior unclean exit, restore its original
    bytes in place so a fresh install can proceed (no game restart needed). Returns True if recovered.
    Safe: only writes when the jmp target is verifiably our trampoline (see _is_leaked_detour). The
    orphaned trampoline page is harmless (a no-op stub) and is reclaimed when the game process exits."""
    base = getattr(scanner, "module_base", None)
    h = next((x for x in HOOKS if x.name == name), None)
    if h is None or base is None:
        return False
    site = base + h.rva
    if not _is_leaked_detour(scanner, site, h.orig):
        return False
    try:
        scanner.write_bytes(site, h.orig)
    except Exception:
        return False
    logger.info("hook %r: recovered a leaked detour @0x%X (restored original bytes from a prior unclean "
                "exit, e.g. the console X button)", name, site)
    return True


def resolve_hook(scanner, name: str) -> Optional[Tuple[int, bytes]]:
    """Current (address, original-bytes) for a hook site: the RVA if its ORIG is intact, else the
    AOB-relocated address if the signature uniquely re-finds it (survives a patch that shifted the RVA),
    else - if the site holds our own leaked detour from a prior unclean exit - the RVA after restoring
    it in place, else None. Gates call this instead of hardcoding base+RVA so they self-heal across both
    game updates and unclean client exits."""
    base = getattr(scanner, "module_base", None)
    h = next((x for x in HOOKS if x.name == name), None)
    if h is None or base is None:
        return None
    site = base + h.rva
    try:
        if scanner.read_bytes(site, len(h.orig)) == h.orig:
            return (site, h.orig)
    except Exception:
        pass
    # Cheap self-heal FIRST: an unclean exit (kill / console-X / crash) leaves OUR OWN detour at the RVA -
    # fast to detect+restore (a 5-byte read + a verify). The AOB fallback below scans the WHOLE module, so
    # doing it first wasted ~module-size per leaked hook and was the dominant cost of a slow first tick after
    # an unclean exit (every site leaked -> one full module scan each = minutes). Order: RVA, leak, AOB.
    if recover_leaked_hook(scanner, name):
        return (site, h.orig)
    if h.aob:
        hits = module_aob_scan(scanner, h.aob)
        if len(hits) == 1:
            return (hits[0], h.orig)
    return None


# The anchor-chain root globals (base+0x2944690 / 0x29446A0) are NOT directly code-referenced - they're
# entries in a .data cluster reached via a near-root global + offset. We harden them by signaturing a
# UNIQUE code ref to a near-root cluster global (base+0x2944450) and applying the fixed cluster delta;
# the whole cluster shifts as a block on a patch, so the deltas hold while the signature re-finds the ref.
ROOT_CLUSTER = {
    "aob": "48 8D 0D ?? ?? ?? ?? E8 ?? ?? ?? ?? 48 8B D0 48 85 C0 74 15 33 C0 89 42 08",  # rva 0x2B4144
    "lea_disp_off": 3, "lea_len": 7, "global_rva": 0x2944450,
    "root_deltas": {0x2944690: 0x240, 0x29446A0: 0x250},  # cluster global -> each anchor root
}
_ROOT_CACHE: Dict[int, int] = {}


def resolve_root(scanner, root_rva: int) -> Optional[int]:
    """Address of an anchor-chain root global, signature-hardened: located via the unique cluster ref +
    fixed delta (survives a patch that shifts the RVA). Cached per process (module addresses are stable
    within a run). Falls back to base+rva if the signature can't resolve (the self-check flags that)."""
    base = getattr(scanner, "module_base", None)
    if base is None:
        return None
    delta = ROOT_CLUSTER["root_deltas"].get(root_rva)
    if delta is None:
        return base + root_rva               # not a known root -> raw RVA
    if root_rva in _ROOT_CACHE:
        return _ROOT_CACHE[root_rva]
    addr = base + root_rva                    # fallback
    hits = module_aob_scan(scanner, ROOT_CLUSTER["aob"])
    if len(hits) == 1:
        try:
            disp = int.from_bytes(scanner.read_bytes(hits[0] + ROOT_CLUSTER["lea_disp_off"], 4), "little", signed=True)
            addr = (hits[0] + ROOT_CLUSTER["lea_len"] + disp) + delta
        except Exception:
            addr = base + root_rva
    _ROOT_CACHE[root_rva] = addr
    return addr


@dataclass
class CheckResult:
    category: str
    name: str
    status: str          # "ok" | "relocated" | "broken" | "garbage" | "unresolved"
    detail: str = ""


def _classify_hook(scanner, h: HookSite) -> CheckResult:
    """Health of one hook site: ok (intact) | relocated (AOB re-found) | leaked (our own detour from an
    unclean exit, auto-recovers) | broken (genuine code change, needs re-RE) | unresolved (unreadable)."""
    site = scanner.module_base + h.rva
    try:
        cur = scanner.read_bytes(site, len(h.orig))
    except Exception:
        return CheckResult("hook", h.name, "unresolved", "site unreadable @0x%X" % site)
    if cur == h.orig:
        return CheckResult("hook", h.name, "ok", "intact @0x%X" % site)
    # ORIG mismatch. Cheap check FIRST: is this our OWN leaked detour (a prior unclean exit, e.g. the
    # console-X close)? That's a 5-byte read + verify. The AOB fallback below scans the WHOLE module, so
    # doing it first cost ~module-size PER leaked hook and dominated the first-tick preflight after an
    # unclean exit (measured ~40s for 3 leaked sites). Order: intact, leak, AOB-relocate.
    if _is_leaked_detour(scanner, site, h.orig):
        return CheckResult("hook", h.name, "leaked",
                           "our detour still installed @0x%X (prior unclean exit) - auto-recovers on next --memory attach" % site)
    # Not our leak - a real patch may have shifted the RVA: try AOB auto-relocation.
    if h.aob:
        hits = module_aob_scan(scanner, h.aob)
        if len(hits) == 1:
            return CheckResult("hook", h.name, "relocated", "moved to 0x%X (rva 0x%X stale)" % (hits[0], h.rva))
        if len(hits) > 1:
            return CheckResult("hook", h.name, "broken", "AOB ambiguous (%d hits)" % len(hits))
    detail = "ORIG changed + AOB not found - re-RE" if h.aob else "ORIG mismatch + no AOB - re-RE (got %s)" % cur.hex()
    return CheckResult("hook", h.name, "broken", detail)


def check_hooks(scanner) -> List[CheckResult]:
    return [_classify_hook(scanner, h) for h in HOOKS]


def check_anchors(scanner, anchor_table) -> List[CheckResult]:
    out = []
    for name, (lo, hi) in ANCHOR_SANITY.items():
        try:
            val = anchor_table.read(scanner, name)
        except Exception as e:
            out.append(CheckResult("anchor", name, "unresolved", "read error: %s" % type(e).__name__)); continue
        if val is None:
            out.append(CheckResult("anchor", name, "unresolved", "chain unresolved"))
        elif lo <= val <= hi:
            out.append(CheckResult("anchor", name, "ok", "value=%s" % round(val, 3)))
        else:
            out.append(CheckResult("anchor", name, "garbage", "value=%s outside [%s,%s] (stale root?)" % (val, lo, hi)))
    return out


def check_research(scanner) -> List[CheckResult]:
    """Reachability of the research items map via the *validated* reader, which tries BOTH master-root
    chains and picks whichever lands on a real map (research.py:_research_system). The primary chain can
    dereference to a valid-but-wrong object on some sessions, so checking only RESEARCH_CHAIN (as this
    did) false-fails - mirror what the client actually does."""
    try:
        from .research import ResearchReader
        snap = ResearchReader(scanner)._snapshot()
    except Exception as e:
        return [CheckResult("system", "research_map", "unresolved", "reader error: %s" % type(e).__name__)]
    if snap is None:
        return [CheckResult("system", "research_map", "garbage",
                            "items map unreadable via either master-root chain (in a loaded zoo?)")]
    cnt = len(snap[0])
    if 0 < cnt < 100000:
        return [CheckResult("system", "research_map", "ok", "%d records" % cnt)]
    return [CheckResult("system", "research_map", "garbage", "record count implausible (%d)" % cnt)]


def check_terrain(scanner, gate=None) -> List[CheckResult]:
    # MAIN2_CODE lives in the writable VM heap, not the module - reuse TerrainGate's writable-region scan.
    # When the client passes its own gate, report from that gate's state WITHOUT a fresh full-heap scan
    # (the in-client gate defers its first scan off the first tick - duplicating it here would re-add the
    # ~20s the defer just removed). Standalone callers (tools/selfcheck.py) pass no gate -> real scan.
    if gate is not None:
        try:
            if gate._cache_valid():
                return [CheckResult("system", "terrain_bytecode", "ok",
                                    "%d copy(ies) of main.2 located" % len(gate._addrs))]
        except Exception:
            pass
        return [CheckResult("system", "terrain_bytecode", "ok",
                            "scan deferred off the first tick (gate locates within a tick or two); "
                            "verify the bytecode on demand with tools/selfcheck.py")]
    try:
        from .terrain import TerrainGate
        found = len(TerrainGate(scanner)._find())
    except Exception as e:
        return [CheckResult("system", "terrain_bytecode", "unresolved", "scan error: %s" % type(e).__name__)]
    if found >= 1:
        return [CheckResult("system", "terrain_bytecode", "ok", "%d copy(ies) of main.2 located" % found)]
    return [CheckResult("system", "terrain_bytecode", "broken", "main.2 signature not found (zoo loaded? bytecode changed?)")]


def check_roots(scanner) -> List[CheckResult]:
    base = scanner.module_base
    hits = module_aob_scan(scanner, ROOT_CLUSTER["aob"])
    if len(hits) != 1:
        return [CheckResult("root", "anchor_roots", "broken",
                            "cluster signature %s (%d hits) - anchors fall back to raw RVA" %
                            ("ambiguous" if hits else "not found", len(hits)))]
    out = []
    for rva in ROOT_CLUSTER["root_deltas"]:
        addr = resolve_root(scanner, rva)
        if addr == base + rva:
            out.append(CheckResult("root", "root_0x%X" % rva, "ok", "signature==RVA @0x%X" % addr))
        else:
            out.append(CheckResult("root", "root_0x%X" % rva, "relocated",
                                   "signature->0x%X (RVA 0x%X stale - anchors will self-correct)" % (addr, base + rva)))
    return out


def check_session(scanner, reader=None) -> List[CheckResult]:
    """AP-session detection state: the park-name marker (park-info +0x1E8) planted by the scenario
    script. 'ok' = marker read back; an unnamed/foreign park is REPORTED but not a failure (the client
    is designed to idle there) - it only flags 'broken' when the park-info class itself is gone.

    When the client passes its own ParkNameReader (already scanned + cached this tick by the session
    gate that fronts every poll tick), reuse it: read() returns from cache, so the preflight doesn't
    repeat the ~20s park-info vtable sweep the session gate just did. Standalone callers pass none."""
    try:
        from .session import AP_PARK_NAME, ParkNameReader
        if reader is None:
            reader = ParkNameReader(scanner)
        name = reader.read()
    except Exception as e:
        return [CheckResult("system", "ap_session", "unresolved", "reader error: %s" % type(e).__name__)]
    if name == AP_PARK_NAME:
        return [CheckResult("system", "ap_session", "ok", "park-name marker present (%r)" % name)]
    if reader._cached:
        return [CheckResult("system", "ap_session", "ok",
                            "park-info resolves; no marker (name=%r) - client idles in this park" % name)]
    return [CheckResult("system", "ap_session", "broken",
                        "no park-info instance (no loaded zoo, or vtable RVA stale after a patch)")]


def run_selfcheck(scanner, anchor_table=None, *, session_reader=None, terrain_gate=None) -> List[CheckResult]:
    """Resolve every dependency and return a health report. anchor_table optional (skips anchor checks).

    session_reader / terrain_gate: the client passes its own already-resolved instances so the preflight
    reuses their state instead of repeating their full-heap scans (~20s each). Standalone callers omit
    them (tools/selfcheck.py) and get fresh scans."""
    results: List[CheckResult] = []
    results += check_hooks(scanner)
    results += check_roots(scanner)
    if anchor_table is not None:
        results += check_anchors(scanner, anchor_table)
    results += check_research(scanner)
    results += check_terrain(scanner, gate=terrain_gate)
    results += check_session(scanner, reader=session_reader)
    return results
