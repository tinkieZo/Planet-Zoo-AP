# ── hook sites the client detours (rva, expected original bytes, role) ───────────────────────────────
@dataclass(frozen=True)
class HookSite:
    name: str
    rva: int
    orig_hex: str
    role: str
    aob: Optional[str] = None        # unique wildcarded pattern for auto-relocation (None until baked)
    script_name: Optional[str] = None  # if script-bound, the registrar name (most robust resolve)

    @property
    def orig(self) -> bytes:
        return bytes.fromhex(self.orig_hex)


